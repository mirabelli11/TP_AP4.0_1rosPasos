document.getElementById('verMasABC').addEventListener('click', function(){
    if(document.getElementById('TareasABC').style.display == '' ||
    document.getElementById('TareasABC').style.display == 'none'){
        document.getElementById('TareasABC').style.display = 'block';
    } else {
        document.getElementById('TareasABC').style.display = 'none';
    }
})
